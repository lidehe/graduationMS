package domain.middle;

public class Sxcjfs {
	private String xuehao;
	private float fs;
	public String getXuehao() {
		return xuehao;
	}
	public void setXuehao(String xuehao) {
		this.xuehao = xuehao;
	}
	public float getFs() {
		return fs;
	}
	public void setFs(float fs) {
		this.fs = fs;
	}
	public Sxcjfs() {
		super();
	}
	public Sxcjfs(String xuehao, float fs) {
		super();
		this.xuehao = xuehao;
		this.fs = fs;
	}
}
